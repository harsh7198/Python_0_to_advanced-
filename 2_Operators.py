# logical operator - and , or ,not

A = True
B = False
print(A or B)
print(A and B)
print(not(A and B))
print("\n")
# comparison operators- <,> , <= , >= , == , != 

print(x>y)
print(x>=y)
print(x<=y)
print(x==y)
print(x!=y)
print(x<y)
print("\n")
